"""
This script takes a whole number from the user and prints out its multiplication table 
with multiples of 2 from 2 up to 10.
"""

whole_num = int(input("Generate a multiplication table for: "))

print("__________")

print("Number: " + str(whole_num))

print("2: " + str(whole_num * 2))
print("4: " + str(whole_num * 4))
print("6: " + str(whole_num * 6))
print("8: " + str(whole_num * 8))
print("10: " + str(whole_num * 10))

print("__________")